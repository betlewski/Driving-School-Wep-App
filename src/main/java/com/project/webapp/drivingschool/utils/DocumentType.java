package com.project.webapp.drivingschool.utils;

/**
 * Typ dokumentu wymaganego w ramach kursu nauki jazdy
 */
public enum DocumentType {

    /**
     * Badania lekarskie (dla każdej kategorii prawa jazdy)
     */
    MEDICAL_EXAMS,

    /**
     * Profil kandydata na kierowcę (dla każdej kategorii prawa jazdy)
     */
    DOCUMENT_PKK,

    /**
     * Zgoda rodziców (tylko dla kursantów poniżej 18 lat)
     */
    PARENT_PERMISSION

}
